function mostrar()
{
    var nombre; //"nombre" significa el nombre que le ponemos a el espacio asignado para guardar los datos

    nombre=prompt("ingrese nombre"); /* prompt crea una ventana para ingresar el valor que le vamos a 
                                        asignar a "nombre" (variable)"ingrese nombre" el texto a mostrar */

    alert (nombre); //alert ejecuta una ventana mostrando el valor dado anteriormente a la variable (nombre)


}

